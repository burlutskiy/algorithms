      A management interface, as defined by JMX, is composed of named objects - called<span class="Apple-converted-space">&#xa0;</span><a style="color: rgb(0, 0, 0)" href="http://java.sun.com/javase/6/docs/technotes/guides/jmx/overview/instrumentation.html#wp998816"><font color="rgb(0, 0, 0)">MBeans</font></a><span class="Apple-converted-space">&#xa0;</span>(Management Beans). MBeans are registered with a name (an<span class="Apple-converted-space">&#xa0;</span><a style="color: rgb(0, 0, 0)" href="http://java.sun.com/javase/6/docs/api/javax/management/ObjectName.html"><font color="rgb(0, 0, 0)">ObjectName</font></a>) in an<span class="Apple-converted-space">&#xa0;</span><a style="color: rgb(0, 0, 0)" href="http://java.sun.com/javase/6/docs/technotes/guides/jmx/overview/agent.html#wp998096"><font color="rgb(0, 0, 0)">MBeanServer</font></a>. To manage (a) resource(s) in your application, you will<span class="Apple-converted-space">&#xa0;</span><a style="color: rgb(0, 0, 0)" href="http://java.sun.com/javase/6/docs/technotes/guides/jmx/overview/architecture.html#wp998959"><font color="rgb(0, 0, 0)">write an MBean that defines its management interface</font></a>, and then register that MBean in your MBeanServer.<br/>The content of the MBeanServer can then be<span class="Apple-converted-space">&#xa0;</span><a style="color: rgb(0, 0, 0)" href="http://java.sun.com/javase/6/docs/technotes/guides/jmx/overview/architecture.html#wp999066"><font color="rgb(0, 0, 0)">exposed through various protocols</font></a>, implemented by<span class="Apple-converted-space">&#xa0;</span><a style="color: rgb(0, 0, 0)" href="http://java.sun.com/javase/6/docs/technotes/guides/jmx/overview/agent.html#wp998899"><font color="rgb(0, 0, 0)">protocol connectors</font></a>, or<span class="Apple-converted-space">&#xa0;</span><a style="color: rgb(0, 0, 0)" href="http://java.sun.com/javase/6/docs/technotes/guides/jmx/overview/agent.html#wp998892"><font color="rgb(0, 0, 0)">protocol adaptors</font></a>.
    </p>
    <p style="margin-left: 0px; margin-bottom: 10px; margin-right: 0px; margin-top: 10px">
      A<span class="Apple-converted-space">&#xa0;</span><u>protocol connector</u><span class="Apple-converted-space">&#xa0;</span>(e.g. the<span class="Apple-converted-space">&#xa0;</span><a style="color: rgb(0, 0, 0)" href="http://java.sun.com/javase/6/docs/technotes/guides/jmx/overview/connectors.html#wp997004"><font color="rgb(0, 0, 0)">JMX RMI Connector</font></a>) exposes the MBeans as they are - so a remote client sees the same model than a local client.<br/>A<span class="Apple-converted-space">&#xa0;</span><u>protocol adaptor</u><span class="Apple-converted-space">&#xa0;</span>(e.g. an SNMP adaptor, or HTML adaptor) performs (or provides hooks to let you perform) a model mediation - to adapt the model to what a client of that protocol (e.g. SNMP Manager, or Web Browser) would expect to see.
    </p>
    <p style="margin-left: 0px; margin-bottom: 10px; margin-right: 0px; margin-top: 10px">
      Today there is one standard connector defined for JMX - which is the<span class="Apple-converted-space">&#xa0;</span><a style="color: rgb(0, 0, 0)" href="http://java.sun.com/javase/6/docs/technotes/guides/jmx/overview/connectors.html#wp997004"><font color="rgb(0, 0, 0)">RMI Connector</font></a>, and comes in two flavors: RMI/JRMP and RMI/IIOP. There's also a<span class="Apple-converted-space">&#xa0;</span><a style="color: rgb(0, 0, 0)" href="https://ws-jmx-connector.dev.java.net/"><font color="rgb(0, 0, 0)">JMX WebServices Connector in the making</font></a><span class="Apple-converted-space">&#xa0;</span>- which is being defined by<span class="Apple-converted-space">&#xa0;</span><a style="color: rgb(0, 0, 0)" href="http://www.jcp.org/en/jsr/detail?id=262"><font color="rgb(0, 0, 0)">JSR 262</font></a>.
